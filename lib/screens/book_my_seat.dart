  final int rowI;
  final int colI;

  const SeatNumber({required this.rowI, required this.colI});

  @override
  bool operator ==(Object other) {
    return rowI == (other as SeatNumber).rowI && colI == (other as SeatNumber).colI;
  }

  @override
  int get hashCode => rowI.hashCode;



  String rows(int rowI){
    String row = '';
      switch(rowI){
        case 1: { row = ('M');}
          break;
        case 2: { row=('N');}
        break;
        case 3: { row=('L');}
        break;
        case 4: { row=('K');}
        break;
        case 5: { row=('J');}
        break;
