        break;
        case 12: { row=('C');}
        break;
        case 13: { row=('B');}
        break;
        case 14: { row=('A');}
        break;
      }
      return row;
  }
  @override
  String toString() {
    return 'Rreshti ${rows(rowI)} Kolona $colI';
